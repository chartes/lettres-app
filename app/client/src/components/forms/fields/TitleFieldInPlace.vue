<template>
  <div class="field-title">
    <field-label v-if="!!label" :label="label" :add-colons="false"/>
    <div class="field field-title__field" v-if="editable && editMode" ref="hover">
      <div class="control">
        <rich-text-editor
                ref="input"
                :multiline="false"
                v-model="value"
                :formats="[['italic','superscript','note']]"
                :tabindex="tabulationIndex"
                @change="inputChanged"
                @on-keyup-enter="clickSave"
                @on-keyup-escape="cancelInput"
        >
          <button
                  class="button is-small"
                  :class="saveButtonClass"
                  :disabled="status === 'disabled'"
                  @click="clickSave"
          >
            <component :is="saveButtonIcon"/>
          </button>
        </rich-text-editor>
      </div>
    </div>

    <div v-else-if="editable && !editMode"  class="field" ref="hover"
         :tabindex="tabulationIndex"
         @click="enterEditMode"
         @mouseover="overField"
         @mouseout="outField">
      <div class=" control">
        <span class=" field-title__input--fake"
              :class="userClass" v-html="value || notSet"/>
        <component class="field-title__icon" :is="editButtonIcon"/>
      </div>
    </div>

    <div v-else>
      <h1 class="" :class="unknownClass" v-html="value || notSet"></h1>
    </div>

  </div>
</template>

<script>
  import FieldLabel from './FieldLabel';
  import TextFieldMixins from './TextFieldMixins';
  import RichTextEditor from "./RichTextEditor";
  import IconSave from '../../ui/icons/IconSave';
  import IconPenEdit from '../../ui/icons/IconPenEdit';
  import IconError from '../../ui/icons/IconError';
  import IconSuccess from '../../ui/icons/IconSuccess';
  export default {
    name: 'TitleFieldInPlace',
    components: {IconSuccess, IconError, IconSave, RichTextEditor, IconPenEdit, FieldLabel},
    mixins: [TextFieldMixins],
    props: {
      status: {
        validator: function (value) {
          return ['normal', 'success', 'error', 'loading', 'disabled' ].indexOf(value) !== -1
        },
        default: 'normal'
      },
      'specificClass' : {
      	default: null
      }
    },
    methods: {
      clickSave () {
        if (this.status === 'error' || this.status === 'success') return;
        this.$emit('changed', { value: this.value, name: this.name, oldValue: this.initialValue })
      },
    },
    computed: {
      saveButtonClass () {
        switch (this.status) {
          case 'normal':
          case 'disabled':
            return 'is-success'
          case 'success':
            return 'is-success'
          case 'error':
            return 'is-danger'
          case 'loading':
            return 'is-loading'
        }
      },
      saveButtonIcon () {
        switch (this.status) {
          case 'normal':
          case 'loading':
          case 'disabled':
            return IconSave;
          case 'success':
            return IconSuccess;
          case 'error':
            return IconError;
        }
      },
      userClass() {
      	return !!this.$props.specificClass ? this.$props.specificClass  : '';
      }
    },
    watch: {
      initialValue (val, oldVal) {
        this.value = val
      },
      status (val) {
        if (val === 'success') {
          this.exitEditMode(true)
        }
      }
    }
  }
</script>

